        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十条</span></span></strong><span style=";font-family:仿宋_GB2312;font-size:21px">&nbsp;&nbsp;<span style="font-family:仿宋_GB2312">创新服务提供机构需提交相关资料到创新券审核发放机构进行资格审核：</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（一）带有统一社会信用代码的营业执照或事业单位法人证书复印件；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（二）服务内容及服务能力的说明材料；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（三）相关资质证明复印件。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">已通过河北省科技创新券审核的创新服务提供机构，在廊坊市创新券审核发放机构备案后直接成为收券机构。</span></span>
    </p>
    <p style=";text-align:center;line-height:40px">
        <span style=";font-family:黑体;font-size:21px"><span style="font-family:黑体">第五章</span> &nbsp;<span style="font-family:黑体">申请与发放</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">二十一</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">科技型中小企业或创新创业团队首次申领创新券，需携带相关资料到创新券审核发放机构进行资格审核与备案。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">科技型中小企业需提交的审核备案材料主要包括</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">：</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（一）带有统一社会信用代码的营业执照复印件；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（二）法定代表人身份证复印件；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（三）有效期内的科技型中小企业认定证书复印件。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">创新创业团队需提交的审核备案材料主要包括：</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（一）负责人身份证复印件等个人信息文件；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（二）入驻众创空间证明。</span></span>
    </p>
    <p style="text-indent: 43px;line-height: 40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十二条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">科技</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">创新券首次申领额度不超过</span>5万元，申领额度使用超过50%后可再次申领。</span>
    </p>
    <p style="text-indent: 43px;line-height: 40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十三条</span></span></strong><span style=";font-family:仿宋_GB2312;font-size:21px">&nbsp;&nbsp;<span style="font-family:仿宋_GB2312">科技创新券原则上每年可多次申领，申领金额上限为</span>10万元。当年创新券专项资金发放完毕后未申领到创新券的企业或创新创业团队，次年优先安排。</span>
    </p>
    <p style="text-indent:0;text-align:center;line-height:40px">
        <span style=";font-family:黑体;font-size:21px"><span style="font-family:黑体">第六章</span> &nbsp;<span style="font-family:黑体">使用</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">二十四</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">持券企业或创新创业团队使用创新券时，必须与收券机构签订书面服务合同，合同中需约定服务内容、期限、价格及科技创新券使用金额等情况，每项合同中，使用创新券额度不得超过全部应付资金的</span>50%，其他费用由持券企业或创新创业团队自行支付。</span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十五条</span></span></strong><span style=";font-family:仿宋_GB2312;font-size:21px">&nbsp;&nbsp;<span style="font-family:仿宋_GB2312">每张科技创新券编号唯一，仅限申领企业或创新创业团队使用，不得转让、买卖和重复使用。持券企业或创新创业团队向收券机构付券时，折抵费用金额达不到面值，也不可由收券机构找付小面值券。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十六条</span></span></strong><span style=";font-family:仿宋_GB2312;font-size:21px">&nbsp;&nbsp;<span style="font-family:仿宋_GB2312">符合条件的收券机构名单、科技型中小企业及创新创业团队创新券申领信息在廊坊市科技信息网进行公示，收券机构在接收科技创新券时，应核实其真实有效性。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十七条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">科技创新券设置有效期，仅限在有效期内使用，过期作废。有效期内创新券使用额度低于申领额度</span>30%（不含30%）的企业或创新创业团队，次年将不能再次申领创新券。</span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十八条</span></span></strong><span style=";font-family:仿宋_GB2312;font-size:21px">&nbsp;&nbsp;<span style="font-family:仿宋_GB2312">超过有效期未使用的科技创新券余额，重新纳入创新券专项资金，在下一个申报周期发放。</span></span>
    </p>
    <p style=";text-align:center;line-height:40px">
        <span style=";font-family:黑体;font-size:21px"><span style="font-family:黑体">第七章</span> &nbsp;<span style="font-family:黑体">兑付</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第二十九条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">科技创新券采用随时兑付方式，收券机构需提交以下材料到审核发放机构进行审核</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">：</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（一）科技创新券；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（二）</span></span><span style=";font-family:宋体;font-size:21px"><span style="font-family:仿宋_GB2312">《</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">廊坊市科技</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">创新券</span></span><span style=";font-family:宋体;font-size:21px"><span style="font-family:仿宋_GB2312">兑</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">付</span></span><span style=";font-family:宋体;font-size:21px"><span style="font-family:仿宋_GB2312">申请表》</span></span><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（三）收券机构与持券企业或创新创业团队签订的创新服务合同复印件，属于技术交易（技术开发、技术转让、技术咨询、技术服务）创新活动的，还需提交技术合同登记站出具的技术合同认定登记证明；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（四）提供完成科技服务的证明材料，如技术解决方案、检验检测认证报告、咨询报告、专利申请书等资料复印件；</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">（五）加盖财务专用章的发票存根及银行进账凭证复印件。</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">三十</span></span></strong><strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">审核发放机构对兑付申请材料的完整性进行审核，对符合要求的申请清单在廊坊市科技信息网公示</span>7日，无异议后由审核兑付机构按照收券机构所收创新券的实际折抵金额进行兑付。</span>
    </p>
    <p style=";text-align:center;line-height:40px">
        <span style=";font-family:黑体;font-size:21px"><span style="font-family:黑体">第八章</span> <span style="font-family:黑体">监督管理</span></span>
    </p>
    <p style="text-indent:43px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第三十一条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">市科技局对创新券使用情况将按照不低于</span>20%的比例抽查。对于通过恶意串通、弄虚作假等行为骗取财政资金的，一经发现，即刻注销该创新券停止兑付，并依法追回已兑付创新券资金，同时取消收券资格，列入不良诚信记录名单，并在今后不再给予各类资金支持，涉嫌违法的，将依法追究相关责任。</span>
    </p>
    <p style=";text-align:center;line-height:40px">
        <span style=";font-family:黑体;font-size:21px"><span style="font-family:黑体">第九章</span> <span style="font-family:黑体">附则</span></span>
    </p>
    <p style="text-indent:42px;line-height:40px">
        <strong><span style="font-family: 仿宋_GB2312;font-size: 21px"><span style="font-family:仿宋_GB2312">第三十二条</span> &nbsp;</span></strong><span style=";font-family:仿宋_GB2312;font-size:21px"><span style="font-family:仿宋_GB2312">为维护企业和创新创业团队的商业机密及合法权益，要求审核发放机构对企业和创新创业团队提交的注册资料、创新活动内容等相关信息严格保密。</span></span>
